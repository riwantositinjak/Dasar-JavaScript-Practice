// menampilkan hello world sebanyak 10 kali di console menggunakan
// pengulangan while 

var a = 1; 
var b = 10; // ini variabel untuk mengecek kondisi pengulangan pada while 


while( a <= b) {
    console.log('Hello World') // mencetak sebuah string 'Hello World' di console
    a++; // ini proses aksi yang menyatakan variabel a akan di increment sesuai kondisi a <= b
}

